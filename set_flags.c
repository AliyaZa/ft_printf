#include "ft_printf.h"

int		set_flags(t_bag *bag, char c)
{
	char	*all;

	if (bag->zero == 0)
		all = "-0";
	else
		all = "-";
	while (*all)
	{
		if (c == *all)
			return (1);
		all++;
	}
	return (0);
}
